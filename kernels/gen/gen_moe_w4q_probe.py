#!/usr/bin/env python3
"""Generate sass/moe_w4q_probe.sass — HW micro-validation of the RADIX-5
split-FP4 decode (moe_w4q_mm building block), one warp, one k64 block:

  in:  d_ref  (320 B: 32 lanes x 8 B "P8" low 64 bits, then 32 x 2 B "P2"
              high 16 bits of the 80-bit lane record = 8 words x 10 bits)
       d_base (256 B: w2 fragment-major 2-bit base codes, 32 lanes x 8 B)
  out: d_out  (32 lanes x 8 words x 4 B decoded e4m3 bytes)

Per word w (4 elems): c = record[10w .. 10w+10) -> base-5 digits d0..d3
(magic /5: q = (x*0x334)>>12, exact for x < 1024, brute-forced) ->
selector nibbles (digit + 5*is_big from the base-code spread) -> one PRMT
over the FULL e2m1 magnitude pool {0,.5,1,1.5 | 2,3,4,6} -> sign OR.
Everything else (spread, PRMT idioms, LDG desc forms) verbatim from the
proven w2/w4s generators. New forms exercised here on purpose:
LDG.E.U16, IMAD negative immediate, PRMT with non-RZ third source,
STG.E immediate offsets.
"""
import sys

OUT = []


def L(s=""):
    OUT.append(s)


def emit_spread(src, bit, dst):
    """byte (4x 2-bit fields) of `src` at `bit` -> nibble-stride selector
    word in R{dst} (verbatim w2/w4s idiom: 4x LOP3 + 3x IMAD)."""
    if bit:
        L(f"    SHF.R.U32.HI R0, RZ, {hex(bit)}, {src} ;")
    else:
        L(f"    MOV R0, {src} ;")
    L("    LOP3.LUT R1, R0, 0x03, RZ, 0xc0, !PT ;")
    L("    LOP3.LUT R2, R0, 0x0c, RZ, 0xc0, !PT ;")
    L("    LOP3.LUT R3, R0, 0x30, RZ, 0xc0, !PT ;")
    L("    LOP3.LUT R0, R0, 0xc0, RZ, 0xc0, !PT ;")
    L("    IMAD R1, R2, 0x4, R1 ;")
    L("    IMAD R1, R3, 0x10, R1 ;")
    L(f"    IMAD R{dst}, R0, 0x40, R1 ;")


def emit_extract10(w, dst):
    """bits [10w, 10w+10) of (R14:R13:R12) -> R{dst} (proven forms only:
    SHF.R.U32.HI with RZ low = plain >>, SHF.L.U32, LOP3)."""
    b = 10 * w
    if b + 10 <= 32:                       # words 0..2: inside R12
        if b:
            L(f"    SHF.R.U32.HI R{dst}, RZ, {hex(b)}, R12 ;")
            src = f"R{dst}"
        else:
            src = "R12"
        L(f"    LOP3.LUT R{dst}, {src}, 0x3ff, RZ, 0xc0, !PT ;")
    elif b < 32:                           # word 3: spans R12/R13
        L(f"    SHF.R.U32.HI R{dst}, RZ, {hex(b)}, R12 ;")
        L(f"    SHF.L.U32 R9, R13, {hex(32 - b)}, RZ ;")
        L(f"    LOP3.LUT R{dst}, R{dst}, R9, RZ, 0xfc, !PT ;")
        L(f"    LOP3.LUT R{dst}, R{dst}, 0x3ff, RZ, 0xc0, !PT ;")
    elif b + 10 <= 64:                     # words 4,5: inside R13
        L(f"    SHF.R.U32.HI R{dst}, RZ, {hex(b - 32)}, R13 ;")
        L(f"    LOP3.LUT R{dst}, R{dst}, 0x3ff, RZ, 0xc0, !PT ;")
    elif b < 64:                           # word 6: spans R13/R14
        L(f"    SHF.R.U32.HI R{dst}, RZ, {hex(b - 32)}, R13 ;")
        L(f"    SHF.L.U32 R9, R14, {hex(64 - b)}, RZ ;")
        L(f"    LOP3.LUT R{dst}, R{dst}, R9, RZ, 0xfc, !PT ;")
        L(f"    LOP3.LUT R{dst}, R{dst}, 0x3ff, RZ, 0xc0, !PT ;")
    else:                                  # word 7: inside R14
        L(f"    SHF.R.U32.HI R{dst}, RZ, {hex(b - 64)}, R14 ;")
        L(f"    LOP3.LUT R{dst}, R{dst}, 0x3ff, RZ, 0xc0, !PT ;")


L("// moe_w4q_probe — radix-5 split-FP4 decode micro-validation")
L("// (GENERATED by gen/gen_moe_w4q_probe.py)")
L(".entry moe_w4q_probe")
L("    .reg R0-R63")
L("    .param u64 d_ref")
L("    .param u64 d_base")
L("    .param u64 d_out")
L()
L("    S2R R40, SR_TID.X ;                      // lane")
L("    LDCU.64 UR4, c[0x0][0x358] ;")
L("    LDC.64 R4, c[0x0][0x380] ;               // d_ref")
L("    LDC.64 R6, c[0x0][0x388] ;               // d_base")
L("    LDC.64 R20, c[0x0][0x390] ;              // d_out")
L()
L("    // P8 ptr = d_ref + lane*8 ; P2 ptr = d_ref + 256 + lane*2")
L("    SHF.L.U32 R0, R40, 0x3, RZ ;")
L("    IMAD.WIDE.U32 R10, R0, 0x1, R4 ;")
L("    SHF.L.U32 R0, R40, 0x1, RZ ;")
L("    IADD3 R0, PT, PT, R0, 0x100, RZ ;")
L("    IMAD.WIDE.U32 R14, R0, 0x1, R4 ;")
L("    // base ptr = d_base + lane*8 ; out ptr = d_out + lane*32")
L("    SHF.L.U32 R0, R40, 0x3, RZ ;")
L("    IMAD.WIDE.U32 R16, R0, 0x1, R6 ;")
L("    SHF.L.U32 R0, R40, 0x5, RZ ;")
L("    IMAD.WIDE.U32 R20, R0, 0x1, R20 ;")
L()
L("    LDG.E.64 R12, desc[UR4][R10.64] ;        // record bits 0-64")
L("    LDG.E.U16 R14, desc[UR4][R14.64] ;       // record bits 64-80")
L("    LDG.E.64 R24, desc[UR4][R16.64] ;        // base codes 8B")
L()
L("    MOV R41, 0x3c383000 ;                    // pool LO: 0,.5,1,1.5 e4m3")
L("    MOV R42, 0x4c484440 ;                    // pool HI: 2,3,4,6 e4m3")
L("    MOV R43, 0x00008080 ;                    // sign pool: codes 0,1 -> 80")
L("    MOV R44, 0x1111 ;                        // nibble bit0 mask")
L()
for w in range(8):
    bsrc = "R24" if w < 4 else "R25"
    bit = (w % 4) * 8
    L(f"    // ---- word {w} ----")
    emit_extract10(w, 30)                     # R30 = c
    L("    IMAD R31, R30, 0x334, RZ ;")
    L("    SHF.R.U32.HI R31, RZ, 0xc, R31 ;         // q0")
    L("    IMAD R32, R31, 0xfffffffb, R30 ;         // d0 = c - 5*q0")
    L("    IMAD R33, R31, 0x334, RZ ;")
    L("    SHF.R.U32.HI R33, RZ, 0xc, R33 ;         // q1")
    L("    IMAD R34, R33, 0xfffffffb, R31 ;         // d1")
    L("    IMAD R35, R33, 0x334, RZ ;")
    L("    SHF.R.U32.HI R35, RZ, 0xc, R35 ;         // q2 = d3")
    L("    IMAD R36, R35, 0xfffffffb, R33 ;         // d2")
    L("    IMAD R32, R34, 0x10, R32 ;               // sel |= d1<<4")
    L("    IMAD R32, R36, 0x100, R32 ;              // sel |= d2<<8")
    L("    IMAD R32, R35, 0x1000, R32 ;             // sel |= d3<<12")
    emit_spread(bsrc, bit, 18)                # R18 = selC (codes, nibbles)
    L("    SHF.R.U32.HI R37, RZ, 0x1, R18 ;")
    L("    LOP3.LUT R37, R18, R37, RZ, 0x3c, !PT ;  // c0^c1 per nibble")
    L("    LOP3.LUT R37, R37, R44, RZ, 0x0c, !PT ;  // ~t & 0x1111 = is_big")
    L("    IMAD R32, R37, 0x5, R32 ;                // sel += 5*is_big")
    L("    PRMT R38, R41, R32, R42 ;                // mag bytes (full pool)")
    L("    PRMT R39, R43, R18, RZ ;                 // sign bytes")
    L("    LOP3.LUT R38, R38, R39, RZ, 0xfc, !PT ;  // OR signs")
    L(f"    STG.E desc[UR4][R20.64+{hex(4 * w)}], R38 ;")
L("    EXIT ;")

path = sys.argv[1] if len(sys.argv) > 1 else "/tmp/moe_w4q_probe.sass"
open(path, "w").write("\n".join(OUT) + "\n")
print(f"wrote {path} ({len(OUT)} lines)")
